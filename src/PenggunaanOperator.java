public class PenggunaanOperator {
    public static void main(String[] args) {
        // Nilai Variabel Yang Digunakan
        int a = 67;
        int b = 80;
        int c = 45;
        //Menampilkan Nilai Variabel
        System.out.println("Nilai Variabel Yang Akan Digunakan Dalam Operator");
        System.out.println("=====================================================");
        System.out.println("Nilai variabel a adalah " + a);
        System.out.println("Nilai variabel b adalah " + b);
        System.out.println("Nilai variabel c adalah " + c);
        System.out.println("=====================================================");
        System.out.println("Menentukan Hasil Lebih Besar Dari ('>')");
        System.out.println("=====================================================");
        System.out.println("Apakah a > b ? Jabawannya : " + (a > b));
        System.out.println("Apakah a > c ? Jabawannya : " + (a > b));
        System.out.println("Apakah b > c ? Jabawannya : " + (b > c));
        System.out.println("=====================================================");
        System.out.println("Menentukan Hasil Lebih Besar Atau Sama Dengan ('>=')");
        System.out.println("=====================================================");
        System.out.println("Apakah a >= b ? Jabawannya : " + (a > b));
        System.out.println("Apakah a >= c ? Jabawannya : " + (a > b));
        System.out.println("Apakah b >= c ? Jabawannya : " + (b > c));
        System.out.println("=====================================================");
        System.out.println("Menentukan Hasil Lebih Kecil ('<')");
        System.out.println("=====================================================");
        System.out.println("Apakah a < b ? Jabawannya : " + (a < b));
        System.out.println("Apakah a < c ? Jabawannya : " + (a < b));
        System.out.println("Apakah b < c ? Jabawannya : " + (b < c));
        System.out.println("=====================================================");
        System.out.println("Menentukan Hasil Lebih Kecil Atau Sama Dengan ('<=')");
        System.out.println("=====================================================");
        System.out.println("Apakah a <= b ? Jabawannya : " + (a <= b));
        System.out.println("Apakah a <= c ? Jabawannya : " + (a <= b));
        System.out.println("Apakah b <= c ? Jabawannya : " + (b <= c));
        System.out.println("=====================================================");
        System.out.println("Menentukan Hasil Sama Dengan ('==')");
        System.out.println("=====================================================");
        System.out.println("Apakah a == b ? Jabawannya : " + (a == b));
        System.out.println("Apakah a == c ? Jabawannya : " + (a == b));
        System.out.println("Apakah b == c ? Jabawannya : " + (b == c));
        System.out.println("=====================================================");
        System.out.println("Menentukan Hasil Tidak Sama Dengan ('!=')");
        System.out.println("=====================================================");
        System.out.println("Apakah a != b ? Jabawannya : " + (a != b));
        System.out.println("Apakah a != c ? Jabawannya : " + (a != b));
        System.out.println("Apakah b != c ? Jabawannya : " + (b != c));
        
    }
}
